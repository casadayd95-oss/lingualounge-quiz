export const CHAPTER_GRAMMAR_QUIZ_SIZE = 20;
const CHAPTER_GRAMMAR_QUIZ_SIZES: Record<number, number> = {
  11: 30,
  12: 30,
};

function getChapterGrammarQuizSize(chapterNumber: number): number {
  return CHAPTER_GRAMMAR_QUIZ_SIZES[chapterNumber] ?? CHAPTER_GRAMMAR_QUIZ_SIZE;
}

function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function hasChapterGrammar(chapterNumber: number): boolean {
  return chapterGrammarBank.some((q) => q.chapter === chapterNumber);
}

export function buildChapterGrammarQuiz(chapterNumber: number): ChapterGrammarQuestion[] {
  const chapterQuestions = chapterGrammarBank.filter((q) => q.chapter === chapterNumber);
  const categories = [...new Set(chapterQuestions.map((q) => q.category))];
  const quizSize = getChapterGrammarQuizSize(chapterNumber);
  const selected: ChapterGrammarQuestion[] = [];

  for (const cat of categories) {
    const pool = shuffle(chapterQuestions.filter((q) => q.category === cat));
    if (pool.length > 0) selected.push(pool[0]);
  }

  const remaining = shuffle(chapterQuestions.filter((q) => !selected.includes(q)));
  while (selected.length < quizSize && remaining.length > 0) {
    selected.push(remaining.shift()!);
  }

  return shuffle(selected.slice(0, quizSize));
}
