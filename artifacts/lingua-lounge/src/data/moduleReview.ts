function getTranslationOptions(current: Word, allWords: Word[]): string[] {
  const wrong = shuffle(allWords.filter((word) => word.english !== current.english))
    .slice(0, 3)
    .map((word) => word.english);
  return shuffle([current.english, ...wrong]);
}

